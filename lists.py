#working with lists. Data structures that helps with working with large data

#lists of friends

friends = ["Bobo","Joan","Wambui","Jojo","Mojojojo"]


print(friends)

print("\n")

print(friends[0]) #access name in index 0

print("\n")
print(friends[-1]) #accesses items from the back of the list...Wambui

print("\n")
print(friends[1:]) #prints elements in index 1 onwards

print("\n")
print(friends[1:4]) #range from index 1 to element before 4

#how to modify element
friends[1] = "cute Bobo"

print(friends)






















